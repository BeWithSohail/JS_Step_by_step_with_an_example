//Back-Tics Syntax

/* 
    Template Literals use back-ticks (``) rather than the quotes ("") to define a string:
*/

// Example 1
let text = `Hello World!`;
console.log(text);

//Example 2 
// With template literals, you can use both single and double quotes inside a string:

let text2 = `He's often called "Johnny"`;
console.log(text2);

// Interpolation
// Template literals provide an easy way to interpolate variables and expressions into strings. The method is called string interpolation. The syntax is:
// Template literals allow variables in strings:
// The syntax is: ${variable}

// Variable Substitutions
var templateLiterals = "template literals";
document.write(`This is the example of ${templateLiterals}`);
 







